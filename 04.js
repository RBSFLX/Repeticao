
let n = 0, a = 0, b = 0, c = 0, d = 0

for (let i = 0; i >= 0; i = n) {

    n = parseInt(prompt(""))

    if (n >= 0 && n <= 25) {
        a++
    }

    if (n >= 26 && n <= 50) {
        b++
    }

    if (n >= 51 && n <= 75) {
        c++
    }

    if (n >= 76 && n <= 100) {
        d++
    }

}

alert("[0-25] = " + a + ". [26-50] = " + b + ". [51-75] = " + c + ". [76-100] = " + d)